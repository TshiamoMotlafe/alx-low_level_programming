#ifndef MYMATHMACROS_H
#define MYMATHMACROS_H

/**
 * SUM - Computes the sum of two numbers x and y.
 * @x: The first number.
 * @y: The second number.
 *
 * Description: This macro calculates the sum of two numbers x and y.
 *
 * Return: The sum of the input numbers.
 */
#define SUM(x, y) ((x) + (y))

#endif /* MYMATHMACROS_H */

