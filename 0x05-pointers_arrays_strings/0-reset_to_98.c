#include <stdio.h>
/**
 * reset_to_98 - Updates the value pointed to by the given pointer to 98.
 * @n: Pointer to an integer whose value needs to be updated.
 *
 * Description: This function updates the value pointed to by the given
 *              pointer to 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
